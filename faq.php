<div class="main">
  <p><span class="faq">Question: Everyone on my Memberlist is displaying as 3 Combat,
  how do I get the stats to display?</span></p>
  <div class="justify">
    <p>Answer: In the Admin Control Panel:</p>
    <ul>
      <li>Click on Edit / Delete / Update</li>
      <li>Highlight the members you wish to update (1-100 Members at a time).</li>
      <li>Click Update Members and wait for the process to complete (it may take up to 1-2 minutes to update).</li>
    </ul>
    <p>Alternatively you can set the Auto Update option under Detailed Info to
    yes and this will update your memberlist once every 24 hours.</p>
  </div>
</div>

<div class="main">
  <p><span class="faq">Question: The combat level is incorrect for one or more
  usernames on my memberlist, why is this happening?</span></p>
  <div class="justify">
    <p>Answer: First, make sure the spelling of the name is correct. Best way to check this
    is by visting the offical RuneScape hiscores and trying it there. Also make
    sure they have combat stats on the offical hiscores. If they don't then
    the RuneHead Hiscores Catalogue can't determine their combat level.</p>
    
    <p>If a member has a combat stat which doesn't display on the
    hiscores since it is too low then the RuneHead Hiscores Catalogue
    won't be able to correctly determine  the combat level. This can't be
    fixed at the moment as it is out of our control.</p>
  </div>
</div>

<div class="main">
  <p><span class="faq">Question: I'm not receiving my password after registering
  or using the forgot password feature. What can I do?</span></p>
  <div class="justify">
    <p>Answer: Usually the emails arrive immediately, but in some cases it can be slowed down
    and take a few minutes to arrive. So first, make sure you give it at least 20
    minutes to check if it arrives. Make sure you also check your email provider's
    junk mail box as the email sometimes goes there. If the email hasn't arrived
    and you can't find it your junk mail box, you can
    <a href="contactus.php">contact us</a> to get it sorted out.</p>
  </div>
</div>

