<div class="main">
  <h1><a id="history" style="text-decoration: none;">History</a></h1>
  <div class="justify">
    <p>The RuneHead Hiscores Catalogue (formerly RSHSC / RuneScape Hiscores
    Catalogue) story begins in October 2005. Back then WestScript
    provided a small memberlist service to a few clans that required a
	memberlist. This service quickly took off and got to a point
    where the demand was getting too high to keep up with. It was decided
    that an automated service was required so any clan / non-clan group could sign up,
    receive a memberlist and customise it how they wanted.<br /><br />
    Coding on this new service began in December 2005 over the Summer
    holidays and was completed and released on the 3rd of February 2006. In
    the few weeks after being released the service already had well over 200
    memberlists.</p>
    
    <p>Since then the RuneHead Hiscores Catalogue has received many updates
    to improve it greatly. These have included a new design, many new
    features and many small to large updates to the code to ensure the
    service runs as fast and as reliable as possible.</p>

    <p>The RuneHead Hiscores Catalogue was originally hosted on
    <a href="http://www.uzzisoft.com">Uzzisoft</a> and eventually moved
    over to <a href="http://www.runehead.com">RuneHead</a> once the service
    was getting too large and required a dedicated server to run.</p>

    <p>These days, the RuneHead Hiscores Catalogue still is regularly updated
    and continues to be one of the most used services in RuneScape.</p>
  </div>
